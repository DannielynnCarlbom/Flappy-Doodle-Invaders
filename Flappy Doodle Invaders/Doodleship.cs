using System;
public class Doodleship
 {
    // Properties to store the X and Y coordinates of the doodleship
    public int X { get; set; }
    public int Y { get; set; }

    // Constructor for the Doodleship class that initializes its X and Y coordinates
    public Doodleship(int x, int y)
    {
        X = x;
        Y = y;
    }
 }